public class User {
    public String username;
    public String password;
    public int credId,userId;

    public User(){}

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public int getCredId() {
        return credId;
    }

    public int getUserId() {
        return userId;
    }
}
